# - Ler um valor numérico inteiro e apresentar uma mensagem informando se o valor 
# é par ou ímpar. Pesquisar o comando em Python que permite identificar se o número 
# é par ou ímpar. 

#Entrada

Valor = int(input('Digite um valor inteiro:'))

if (Valor % 2 == 0):
    print('%d é par' % Valor)
else:
    print ('%d é  ímpar'% Valor)





